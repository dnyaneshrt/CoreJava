package basic_programs;

public class DataTypeRange {
    public static void main(String[] args) {
        System.out.println("byte min range is :"+Byte.MIN_VALUE+" byte max range is ="+Byte.MAX_VALUE);
        System.out.println("int min range is :"+Integer.MIN_VALUE+" int max range is ="+Integer.MAX_VALUE);
        System.out.println("double min range is :"+Double.MIN_VALUE+" byte max range is ="+Double.MAX_VALUE);
        System.out.println("float min range is :"+Float.MIN_VALUE+" int max range is ="+Float.MAX_VALUE);
        System.out.println("short min range is :"+Short.MIN_VALUE+" short max range is ="+Short.MAX_VALUE);

    }
}
